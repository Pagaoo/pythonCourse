'''
+, -, *, /, //, **, %, ()
// - Divisão inteira, arredonda resultado
** - Porenciação, eleva um número ao outro
% - resultado de divisão
() - Altera a ordem de fazer a operação
'''
print('Adição 10 + 10:', 10 + 10)  # Adição
print('Multiplicação 10 * 10:', 10 * 10)  # Multiplicação
print('Potenciação 10**10:', 10**10)
print('Multiplicação de tipos:', 10 * 'Gabriel')
print('Subtração 10 - 10:', 10-10)  # Subtração
print('Divisão 10/10:', 10 / 10)
print('Divisão inteira 15.5//10:', 15.5 // 10)
print('Resto de divisão(Modulo) 15%10:', 15 % 10)
