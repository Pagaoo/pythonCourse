""""
A função sep= separa os elementos por determinado elemento, Ex: sep='-', separa por - e não mais por espaços
A função end= adiciona algo ao final da linha, se deixado vazio: end='' retira a quebra de linha padrão do print
"""
print('Gabriel', 'Rodrigo', sep='-', end='')
print('João', 'e', 'Maria', sep='-')
#  print('824.176.070-18')
print('824', '176', '070', sep='.', end='-')
print('18')